// country-type.ts
import { TFunction } from "i18next";

export class Country {
    private static readonly _countries: Country[] = [];

    public static readonly ALL: Record<string, Country> = {};

    public static readonly countries: Country[] = Country._countries;

    private constructor(public readonly code: string, public readonly name: string) {
        Country._countries.push(this);
        Country.ALL[code.toUpperCase()] = this;
    }

    public static fromCode(code: string): Country {
        const country = Country.ALL[code.toUpperCase()];
        if (!country) {
            throw new Error(`Unknown country code: ${code}`);
        }
        return country;
    }

    public static getCountryOptions(t: TFunction): { value: string; label: string }[] {
        return Country.countries.map((country) => ({
            value: country.code,
            label: t(`countries:${country.code.toLowerCase()}`, {
                defaultValue: country.name,
            }),
        }));
    }
}

